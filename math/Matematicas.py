import math

#valor minimo
x = min(30,26,5)
print(f"Tu valor minimo es:{x}")
#valor maximo
y = max(100,-155,300)
print(f"Tu valor maximo es:{y}")
#valor absoluto
a = abs(-150)
print(a)
#potencia con base 2
b = pow(4,2)
print(b)

raiz = math.sqrt(144)
print(raiz)

#Funciones trigonometricas
num = math.radians(4)

seno = math.sin(num)
print(seno)

coseno = math.cos(num)
print(coseno)

tangente = math.tan(num)
print(tangente)
